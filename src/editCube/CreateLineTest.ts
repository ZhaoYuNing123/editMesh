import { Behaviour, EngineFactory } from "@egret/engine";
import { component } from "@egret/ecs";
import { MeshFilter, MeshRenderer, DefaultMaterials } from "@egret/render";
import { createLine } from "./LineCreater";

@component()
export class CreateLineTest extends Behaviour {
    onStart() {

        const newEntity = EngineFactory.createGameEntity3D("testLine")
        const meshFilter = newEntity.getOrAddComponent(MeshFilter);
        const meshRenderer = newEntity.getOrAddComponent(MeshRenderer);
        meshFilter.mesh = createLine([0, 0, 0], [1, 1, 1])
        meshRenderer.material = DefaultMaterials.MESH_NORMAL;
    }

}



